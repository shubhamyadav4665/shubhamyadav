num_1=int(input("Enter Num_1: "))
num_2=int(input("Enter Num_2:"))
#---Arithmetic Operator---
print("{0}+{1}={2}".format(num_1,num_2,num_1+num_2))
print("{0}-{1}={2}".format(num_1,num_2,num_1-num_2))
print("{0}*{1}={2}".format(num_1,num_2,num_1*num_2))
print("{0}/{1}={2}".format(num_1,num_2,num_1/num_2))
print("{0}%{1}={2}".format(num_1,num_2,num_1%num_2))
#---Raltional Operator---
print("{0}={1}is{2}".format(num_1,num_2,num_1==num_2))
print("{0}not={1}is{2}".format(num_1,num_2,num_1!=num_2))
print("{0}>{1}is{2}".format(num_1,num_2,num_1>num_2))
print("{0}<{1}is{2}".format(num_1,num_2,num_1<num_2))
print("{0}>={1}is{2}".format(num_1,num_2,num_1>=num_2))
print("{0}<={1}is{2}".format(num_1,num_2,num_1<=num_2))
#---Logical Operator---
print("num_1&num_2:",num_1&num_2)
print("num_1|num_2:",num_1|num_2)
